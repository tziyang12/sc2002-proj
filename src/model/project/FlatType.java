package model.project;

/**
 * The {@code FlatType} enum represents the different types of flats available in the BTO system.
 * Currently, the system supports two types of flats: 
 * {@link #TWO_ROOM} and {@link #THREE_ROOM}.
 */
public enum FlatType {
    /**
     * Represents a 2-room flat type.
     */
    TWO_ROOM,

    /**
     * Represents a 3-room flat type.
     */
    THREE_ROOM
}
